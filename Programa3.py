Cantidad1 = int(input("Dime una primera cantidad: "))
Cantidad2 = int(input("Dime una segunda cantidad: "))
if Cantidad1 > Cantidad2:
    print ("Tu primera cantidad es mayor que la segunda cantidad") 
elif Cantidad2 > Cantidad1:
    print ("Tu segunda cantidad es mayor que la primera cantidad")
else: 
    print ("Tus cantidades son iguales")